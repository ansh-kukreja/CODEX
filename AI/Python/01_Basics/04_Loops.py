
# While Loop
i = 0

while(i < 10):
    print(i)
    i += 1



# For Loop
name = "Ansh"

for c in name:
    print(c)



# For checking presence of a value

if 'n' in name:
    print("'n' exists in ", name)




# For in Range Loop

for i in range(10):
    print(i)



# Range function
# range(start, end, step)

for i in range(5):
    print(i)

for i in range(5, 7):
    print(i)

for i in range(7, 18, 2):
    print(i)