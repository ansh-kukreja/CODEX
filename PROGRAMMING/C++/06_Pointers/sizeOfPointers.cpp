#include<iostream>
using namespace std;

int main(){
    int* a;
    float* b;
    char* c;
    bool* d;

    cout<<"Size of Int Pointer: "<<sizeof(a)<<endl;
    cout<<"Size of Float Pointer: "<<sizeof(b)<<endl;
    cout<<"Size of Char Pointer: "<<sizeof(c)<<endl;
    cout<<"Size of Bool Pointer: "<<sizeof(d)<<endl;
}